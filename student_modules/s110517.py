def process(message: str) -> str:
    if message == "debug":
        return message
    if message == "資訊社":
        return "讚"
    if message == "資訊老師":
        return "帥"
    if message == "資訊社的同學":
        return "棒"
    if message == "資訊社的電腦":
        return "不好說"
    if message == "原神":
        return "起痛"
    if message == "你好":
        return "你好笨"
